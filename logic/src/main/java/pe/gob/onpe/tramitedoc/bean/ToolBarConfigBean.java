public class ToolBarConfigBean {
    private String tipoToolBar;
    private String coEstado;
    private String coTipoDoc;
    private String coFormatoDoc;
    private String inOpcionMP;

    public String getTipoToolBar() {
        return tipoToolBar;
    }

    public void setTipoToolBar(String tipoToolBar) {
        this.tipoToolBar = tipoToolBar;
    }

    public String getCoEstado() {
        return coEstado;
    }

    public void setCoEstado(String coEstado) {
        this.coEstado = coEstado;
    }

    public String getCoTipoDoc() {
        return coTipoDoc;
    }

    public void setCoTipoDoc(String coTipoDoc) {
        this.coTipoDoc = coTipoDoc;
    }

    public String getCoFormatoDoc() {
        return coFormatoDoc;
    }

    public void setCoFormatoDoc(String coFormatoDoc) {
        this.coFormatoDoc = coFormatoDoc;
    }

    public String getInOpcionMP() {
        return inOpcionMP;
    }

    public void setInOpcionMP(String inOpcionMP) {
        this.inOpcionMP = inOpcionMP;
    }
    

    
}
